package JavaClasswork.Lesson1.Lesson7;

public class ApplicationMove {
    private int floor;
    private int weight;

    public ApplicationMove(int floor, int weight) {
        this.floor = floor;
        this.weight = weight;
    }

    public int getFloor() {
        return floor;
    }

    public int getWeight() {
        return weight;
    }
}
